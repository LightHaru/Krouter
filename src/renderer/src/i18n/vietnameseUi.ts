function preserveOuterWhitespace(original: string, translated: string): string {
  const prefix = original.match(/^\s*/)?.[0] || ''
  const suffix = original.match(/\s*$/)?.[0] || ''
  return `${prefix}${translated}${suffix}`
}

export function translateVietnameseText(input: string): string {
  const trimmed = input.trim()
  if (!trimmed) return input
  let translated = extraExactText[trimmed] || exactText[trimmed] || trimmed
  for (const [pattern, replacement] of [...phraseReplacements, ...extraPhraseReplacements]) {
    translated = translated.replace(pattern, replacement)
  }
  return translated === trimmed ? input : preserveOuterWhitespace(input, translated)
}

function translateNodeText(node: Text): void {
  const next = translateVietnameseText(node.nodeValue || '')
  if (next !== node.nodeValue) node.nodeValue = next
}

function translateElementAttributes(element: Element): void {
  for (const attr of ['title', 'placeholder', 'aria-label', 'alt']) {
    const value = element.getAttribute(attr)
    if (!value) continue
    const next = translateVietnameseText(value)
    if (next !== value) element.setAttribute(attr, next)
  }
}

function translateTree(root: Node): void {
  if (root.nodeType === Node.TEXT_NODE) {
    translateNodeText(root as Text)
    return
  }
  if (root.nodeType !== Node.ELEMENT_NODE && root.nodeType !== Node.DOCUMENT_NODE) return

  const element = root as Element
  if (element.nodeType === Node.ELEMENT_NODE) {
    const tag = element.tagName
    if (tag === 'SCRIPT' || tag === 'STYLE' || tag === 'TEXTAREA') return
    translateElementAttributes(element)
  }

  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT | NodeFilter.SHOW_ELEMENT)
  let current = walker.nextNode()
  while (current) {
    if (current.nodeType === Node.TEXT_NODE) {
      translateNodeText(current as Text)
    } else if (current.nodeType === Node.ELEMENT_NODE) {
      translateElementAttributes(current as Element)
    }
    current = walker.nextNode()
  }
}

export function installVietnameseUi(): void {
  document.documentElement.lang = 'vi'
  document.title = APP_NAME

  const originalAlert = window.alert.bind(window)
  window.alert = (message?: unknown): void => originalAlert(translateVietnameseText(String(message ?? '')))
  const originalConfirm = window.confirm.bind(window)
  window.confirm = (message?: string): boolean => originalConfirm(translateVietnameseText(String(message ?? '')))

  const run = (): void => translateTree(document.body)
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', run, { once: true })
  } else {
    run()
  }

  const observer = new MutationObserver((mutations) => {
    for (const mutation of mutations) {
      for (const node of mutation.addedNodes) translateTree(node)
      if (mutation.type === 'characterData') translateTree(mutation.target)
      if (mutation.type === 'attributes') translateTree(mutation.target)
    }
  })
  observer.observe(document.documentElement, {
    childList: true,
    subtree: true,
    characterData: true,
    attributes: true,
    attributeFilter: ['title', 'placeholder', 'aria-label', 'alt']
  })
}
